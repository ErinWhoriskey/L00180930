/**
 * Represents a game character in the system.
 */
public class GameCharacter {

    private int health;

    /**
     * Gets the health value.
     * @return health
     */
    public int getHealth() {
        return health;
    }

    /**
     * Sets the health value.
     * @param health character health
     */
    public void setHealth(int health) {
        this.health = health;
    }
}
